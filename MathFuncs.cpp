#include "MathFuncs.h"

// Definition for add function declared in header
double add(double a, double b)
{
	return a + b;
}

// Definition for subtract function declared in header
double subtract(double a, double b)
{
	return a - b;
}

// Definition for multiply function declared in header
double multiply(double a, double b)
{
	return a * b;
}

// Definition for divide function declared in header
double divide(double a, double b)
{
	return a / b;
}
